#include "rec.h"

void	rush(int x, int y)
{
	int i, j;
	for (i = 1; i <= x; i++)
	{
		for (j = 1; j <= y; j++)
		{
			if (i == 1 || i == x || j == 1 || j == y)
				ft_putchar('x');
			else
				ft_putchar(' ');
		}
	}
}

